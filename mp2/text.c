#include "text.h"


/* A buffer that holds a graphical image of the ASCII characters in the string */
unsigned char text_buffer[STATUS_BAR_SCREEN_SIZE];

/*
 * write_text_to_graph
 *     DESCRIPTION: Produce a buffer that holds a graphical image of the ASCII characters in the string.
 *     INPUTS: str -- the input string
 *             position -- 1 for room name (left)
 *                      -- 2 for typed string (right)
 *                      -- 3 for status message (center)
 *     OUTPUTS: none
 *     RETURN VALUE: none
 *     SIDE EFFECTS: build the text buffer
 */
void write_text_to_graph(const char* str, int position) {

    int start; /* the position of string should start to be printed */

    /* copy the input string */
    char str_copy[strlen(str)];
    strcpy(str_copy, str);

    /* calculate the start position for each position */
    if(position == 1){
        memset(text_buffer, 0x03, STATUS_BAR_SCREEN_SIZE); /* reset text buffer */
        start = 0;
    }
    else if(position == 2){
        if(strlen(str_copy) < 20){ /* 20 is maximum length of input string */
            strcat(str_copy, "_");
        }
        start = SCREEN_WIDTH - FONT_WIDTH * strlen(str_copy);
    }
    else if(position == 3){
        memset(text_buffer, 0x03, STATUS_BAR_SCREEN_SIZE); /* reset text buffer */
        start = (SCREEN_WIDTH - FONT_WIDTH * strlen(str_copy)) / 2;
    }

    int i;   /* loop index over characters             */
    int row; /* loop index over rows of each character */
    int col; /* loop index over cols of each character */

    int c;          /* the current character                                 */
    int c_rowdata;  /* the data of a row of the current character            */
    int mask;       /* used to get the bit of each vertical position         */
    int p_off;      /* offset of plane of first pixel                        */
    int index;      /* the index we need to put the pixel in the text buffer */

    /* put the string in the text buffer in the correct position */
    for(i = 0; i < strlen(str_copy); i++){
        c = (int) str_copy[i];

        /* put each character in the text buffer */
        for(row = 0; row < FONT_HEIGHT; row++){
            c_rowdata = font_data[c][row];
            mask = 0x80;

            /* put each row of the character in the text buffer */
            for(col = 0; col < FONT_WIDTH; col++){
               if(c_rowdata & mask){
                    p_off = (3 - (col & 3));
                    index = p_off * STATUS_BAR_SCROLL_SIZE + (row + 1) * SCROLL_WIDTH + (start + 8 * i + col) / 4;
                    text_buffer[index] = 0x3C;
               }
               mask >>= 1;
            }
        }
    }
}


